								<h3>历史沿革</h3>
								<p>学校缘起于庐山白鹿洞书院，肇基于1940年创建的国立中正大学，1949年更名为南昌大学，1953年改为江西师范学院，1983年更名为江西师范大学，是江西省本科办学历史最为悠久的普通高等院校。2003年，江西金融职工大学（江西银行学校）整建制并入。</p>
								<h3>办学思想</h3>
								<p>学校先后七次迁址，六易其名，四度调整，砥砺出“静思笃行、持中秉正”的校训，孕育了“团结、勤奋、求是、创新”的优良校风，形成了“以人为本、面向社会”的办学思想，“以生为本、以德为先”的育人思想，“百折不挠、艰苦创业”的办学传统，“质量立校、学术铸校、人才强校、特色名校、管理兴校、和谐荣校”的重点任务和“爱国荣校、民主和谐、求真务实、开放创新”的师大精神。.</p>
								<h3>学科建设</h3>
								<p> 学校是博士学位授予单位和全国第一批学士、硕士学位授予单位。现有1个学科（化学）进入ESI全球前1%的，4个博士后流动站，1个博士后科研工作站，3个博士学位授权一级学科点，26个博士学位授权二级学科点，26个硕士学位授权一级学科点，36个硕士学位授权二级学科点（不含一级学科覆盖点），15个硕士专业学位授予点及在职人员以同等学力申请硕士学位授予权。</p>
